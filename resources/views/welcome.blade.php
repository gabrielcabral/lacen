        <div><strong>Análises e Ensaios</strong></div>
        <div>Atualmente, o LACEN-DF realiza cerca de 250 análises, contemplando as áreas de biologia médica, medicamentos, toxicologia e controle de qualidade em produtos e ambientes.</div>
        <div>Os tipos de ensaios / análises atualmente realizados no LACEN-DF evoluíram de acordo com as demandas de saúde da população, bem como da necessidade de adaptação aos novos métodos analíticos. O LACEN-DF vem passando por uma efetiva modernização da instituição, com o objetivo de ofertar uma maior variedade de ensaios com vistas à melhor garantia de controle da saúde da população. Esses serviços dependem de solicitação e encaminhamento médico nas unidades de saúde do Distrito Federal e/ou da ação “in loco” das vigilâncias sanitária e ambiental, para coleta dos materiais a serem analisados.</div>
        <div>O LACEN-DF atua como Laboratório de Referência Regional para os diagnósticos laboratoriais de alguns agravos, tais como hanseníase, leptospirose, coqueluche, sarampo, rubéola, dengue, zika vírus, Chikungunya, difteria, enteroinfecções bacterianas (salmonelose, febre tifóide e cólera), febre amarela, meningites bacterianas, tuberculose, citomegalovírus, hantavirose, H1N1, HIV, contagem de células CD4+/CD8+, hepatites, e, ainda, infecções causadas por fungos que acometem pele, cabelo e unhas, helmintos e protozoários, tais como, malária, chagas, leishmaniose e filariose. Além de ações de controle da qualidade de alimentos, água de consumo humano, águas envasadas e não envasadas, águas de diálise e amostras ambientais, tais como solo e água de mananciais. Atua também complementando ações de investigação de surtos alimentares. Além do monitoramento terapêutico de medicamentos relacionados à saúde mental, transplantes e oncologia.</div>

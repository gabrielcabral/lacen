        <p>&nbsp;</p>
        <p>O Laboratório Central de Saúde Pública do Distrito Federal (LACEN-DF) é o laboratório de referência vinculado à Secretaria de Estado da Saúde do Distrito Federal. O LACEN-DF tem como função básica realizar o diagnóstico laboratorial oportuno, seguro e rápido a fim de contribuir para o controle epidemiológico e sanitário de uma população.</p>
        <p>&nbsp;</p>
        <p>No cumprimento de suas funções, ainda realiza diagnósticos clínicos e epidemiológicos a partir de amostras oriundas de pacientes suspeitos de doenças e atende à demanda analítica de produtos sujeitos à vigilância sanitária como alimentos, medicamentos e saneantes. O laboratório se constitui como uma unidade que participa ativamente das ações de saúde, integrado às discussões e decisões do SUS, cabendo-lhe a responsabilidade de garantir diagnósticos adequados e de qualidade, em tempo hábil, além de contribuir para o desenvolvimento de políticas nacionais para os laboratórios de saúde pública.</p>
        <p>As análises realizadas no LACEN-DF podem ter caráter fiscal e de orientação sobre os produtos e serviços de interesse em vigilância sanitária. Essas análises visam verificar a ocorrência de desvios de qualidade de produtos (alimentos, medicamentos, cosméticos, saneantes e produtos para a saúde) ou matérias-primas analisados em programas de monitoramento, em análises de rotina ou em casos de denúncias, subsidiando as avaliações de risco sanitário.</p>
        <p>A Diretoria do LACEN-DF tem como política estratégica a melhoria contínua da qualidade de seus serviços no cumprimento da sua missão, com o objetivo de garantir a oferta de serviços laboratoriais de qualidade, visando à melhoria do nível de satisfação de seus clientes.</p>
